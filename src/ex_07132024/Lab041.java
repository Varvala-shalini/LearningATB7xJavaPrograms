package ex_07132024;

public class Lab041 {
    public static void main(String[] args) {
        //compound operator
        //+=,-=,*=,/=,%=
        int a = 10;
        a+=10; //a = a+10
        a-=10; //a = a-10
        a*=10; //a = a*10
        a/=10; //a = a/10
        a%=10; //a = a%10
    }
}
